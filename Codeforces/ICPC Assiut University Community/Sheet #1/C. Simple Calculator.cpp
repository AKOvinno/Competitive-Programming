#include<bits/stdc++.h>
using namespace std;

int main()
{
    long long num1, num2;
    cin >> num1 >> num2;
    cout << num1 << " + " << num2 << " = " << num1 + num2 << "\n";
    cout << num1 << " * " << num2 << " = " << num1 * num2 << "\n";
    cout << num1 << " - " << num2 << " = " << num1 - num2 << "\n";
    return 0;
}
